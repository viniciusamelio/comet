const MANIFEST_VERSION: u32 = 1;

#[derive(Debug, Serialize, PartialEq, Eq)]
pub struct RpcManifest {
    pub version: u32,
    pub routes: Vec<RpcRoute>,
}

#[derive(Debug, Serialize, PartialEq, Eq)]
pub struct RpcRoute {
    pub name: String,
    pub module_path: Vec<String>,
    pub source: String,
    pub method: String,
    pub path: String,
    pub data_param: Option<String>,
    pub path_params: Vec<RpcParam>,
    pub body: Option<String>,
    pub response: Option<String>,
    pub error: Option<String>,
    pub auth: RpcAuth,
    pub support: RpcRouteSupport,
    pub warnings: Vec<String>,
}

#[derive(Debug, Serialize, PartialEq, Eq)]
pub struct RpcParam {
    pub name: String,
    pub rust_type: String,
    pub variadic: bool,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct TsModel {
    pub name: String,
    pub fields: Vec<TsModelField>,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct TsModelField {
    pub name: String,
    pub rust_type: String,
    pub ts_type: String,
    pub optional: bool,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct TsEnum {
    pub name: String,
    pub variants: Vec<String>,
}

#[derive(Debug, Clone, Default, PartialEq, Eq)]
pub struct TsTypes {
    pub models: BTreeMap<String, TsModel>,
    pub enums: BTreeMap<String, TsEnum>,
}

#[derive(Debug, Serialize, PartialEq, Eq)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum RpcAuth {
    None,
    Optional,
    Required,
    Authorized {
        policy: Option<String>,
        roles: Vec<String>,
        permissions: Vec<String>,
        scopes: Vec<String>,
        resource: Option<String>,
        mode: AuthMode,
    },
}

#[derive(Debug, Clone, Serialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum AuthMode {
    All,
    Any,
}

#[derive(Debug, Serialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum RpcRouteSupport {
    Json,
    Raw,
    Unsupported,
}

#[derive(Debug)]
struct RocketRouteAttr {
    method: String,
    path: String,
    data_param: Option<String>,
}

struct RocketRouteArgs {
    path: LitStr,
    data_param: Option<String>,
}

impl Parse for RocketRouteArgs {
    fn parse(input: ParseStream<'_>) -> syn::Result<Self> {
        let path = input.parse::<LitStr>()?;
        let mut data_param = None;

        while input.peek(Token![,]) {
            input.parse::<Token![,]>()?;
            if input.is_empty() {
                break;
            }

            let key = input.parse::<syn::Ident>()?;
            input.parse::<Token![=]>()?;
            let value = input.parse::<LitStr>()?;
            if key == "data" {
                data_param = Some(strip_angle_binding(&value.value()).to_owned());
            }
        }

        Ok(Self { path, data_param })
    }
}

pub fn discover_manifest(project_dir: &Path) -> Result<RpcManifest> {
    let src_dir = project_dir.join("src");
    let mut routes = Vec::new();
    visit_dir(&src_dir, &[], &mut routes)?;
    let mount_prefixes = discover_mount_prefixes(&src_dir)?;
    apply_mount_prefixes(&mount_prefixes, &mut routes);
    resolve_authorization_policies(project_dir, &mut routes)?;
    routes.sort_by(|a, b| {
        a.module_path
            .cmp(&b.module_path)
            .then_with(|| a.name.cmp(&b.name))
            .then_with(|| a.method.cmp(&b.method))
    });

    Ok(RpcManifest {
        version: MANIFEST_VERSION,
        routes,
    })
}

